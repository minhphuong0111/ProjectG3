    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Pattern pattern = Pattern.compile("\\d{1,10}");
        if(pattern.matcher(txtPort.getText()).matches() && !txtHost.getText().equals("") && !txtPort.getText().equals("") && !txtDB.getText().equals("") && !txtuser.getText().equals("") )//port la gia tri so
        {
            CheckConnection chkConnect = new CheckConnection();
            try {
                String spwd = new String(txtpwd.getPassword());
                chkConnect.ConnectDB(txtHost.getText(), txtPort.getText(), txtDB.getText(), txtuser.getText(), spwd);
               
                conn = chkConnect.getConn();
                if(conn != null)
                {
                    //ket noi thanh cong luu lai bien conn 
//                    this.frmlogin.conn = conn;
//                    this.setVisible(false);
//                    this.frmlogin.setVisible(true);
                    //System.out.println(""+conn);
                    Home.conn = this.conn;
                    new Home().setVisible(true);
                    this.conn.close();
                    this.dispose();
                    
                }
                else {
                    //ket noi that bai hien thi thong bao va cho nguoi dung nhap lai
                    //kiem tra ket noi voi server
                    String urlDB = "jdbc:sqlserver://"+txtHost.getText()+":"+txtPort.getText()+";";
                    this.conn = DriverManager.getConnection(urlDB, txtuser.getText(), spwd);
                    if(this.conn != null)
                    {
                        //ket noi thanh cong voi server
                        //kiem tra dong y tao csdl voi ten vua nhap
                        int checkJoptionPane;
                        checkJoptionPane = JOptionPane.showConfirmDialog(this, "Do you want to create new database with name is"+txtDB.getText(), "Create Database", NORMAL);
                        if(checkJoptionPane == 0)
                        {
                            boolean flag;
                            //choose YES, create new DB
                            WorkWithDB objWorkWithDB = new WorkWithDB();
                            objWorkWithDB.setConn(conn);
                            objWorkWithDB.setDBName(txtDB.getText());
                            flag = objWorkWithDB.CreateBD(txtDB.getText());
                            if(flag)
                            {//tao csdl thanh cong
                                WriteXMLFile objWriteXMLFile = new WriteXMLFile();
                                objWriteXMLFile.WriteFile(txtHost.getText(), txtPort.getText(), txtDB.getText(), txtuser.getText(), spwd);
                                Home.conn = this.conn;
                                Home.conn.setCatalog(txtDB.getText());
                                new Home().setVisible(true);
                                this.dispose();
                            }
                            else{
                                JOptionPane.showMessageDialog(this, "Cannot create database with "+txtDB.getText());
                                txtDB.setText("");
                            }
                        }
                        else
                        {//khong tao csdl moi
                            this.conn.close();
                            //nguoi dung nhap lai thong tin
                            txtDB.setText("");
                            txtHost.setText("");
                            txtPort.setText("");
                            txtpwd.setText("");
                            txtuser.setText("");
                        }
                    }
                }
            } catch (Exception ex) {
                System.out.println("connect 287"+ex.toString());
            }
        }
        else{
            //port chua gia tri khong phai so
            JOptionPane.showMessageDialog(null, "Port must be number");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

