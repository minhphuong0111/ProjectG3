    private void jfblocklistComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_jfblocklistComponentShown
        
            //block list
            String[] blocklistColumn = new String[]{"Cidentity","Cname","Cstatus"};
            String[] blocklistType = new String[]{"String","String","int"};
            
            ResultSet rsblock = null;
            QueryGeneral objQueryGenblock = new QueryGeneral("Customer");
            objQueryGenblock.setConn(Home.conn);
            rsblock = objQueryGenblock.selectMultiTable("SELECT Cidentity,Cname, Cstatus FROM Customer WHERE Cstatus > 0");
            ResultSet rstempblock ;
            rstempblock = objQueryGenblock.selectMultiTable("SELECT Cidentity,Cname, Cstatus FROM Customer WHERE Cstatus > 0");
            int countblock = 0;
            try {
                while(rstempblock.next())
                {
                    countblock++;
                }
            } catch (SQLException ex) {
                Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
            }
            String[][] sqlDatablock = new String[countblock][blocklistColumn.length];

            Display objDisplayblock = new Display(blocklistColumn);
            sqlDatablock = objDisplayblock.getData(rsblock, blocklistType, countblock, blocklistColumn.length);

            //add to jtable
            dtmBlocklist = (DefaultTableModel)tbBlocklist.getModel(); //model duoc gan khi tao bang
            for(int i = 0; i<countblock; i++)
            {
                dtmBlocklist.addRow(sqlDatablock[i]);
            }
    }//GEN-LAST:event_jfblocklistComponentShown

