        //top
        String sqltop = "SELECT TOP(20) Bname "
                + "From Book, BorrowDetail "
                + "Where Book.BookID = BorrowDetail.BookID "
                + "GROUP BY Bname "
                + "order by Count(number) desc";
        rstop = qg.selectMultiTable(sqltop);
        String[] topcolumn = new String[]{"Bname"};
        String[] toptype = new String[]{"String"};
        ResultSet rstoptemp ;
        rstoptemp = qg.selectQuery(sqltop);
        int count = 0;
        try {
            while(rstoptemp.next())
            {
                count++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
        String[][] sqlData = new String[count][topcolumn.length];
        
        objDisplay = new Display(topcolumn);
        sqlData = objDisplay.getData(rstop, toptype, count, topcolumn.length);
        
        //add to jtable
        dtmtop = (DefaultTableModel)tbTop.getModel();
        for(int i = 0; i<count; i++)
        {
            dtmtop.addRow(sqlData[i]);
        }
        //End display top
        
        //new
        String sqlnew = "SELECT TOP(20) Bname "
                + "From Book, BorrowDetail "
                + "Where Book.BookID = BorrowDetail.BookID "
                + "GROUP BY Bname "
                + "order by Count(number) desc";
        rsnew = qg.selectMultiTable(sqlnew);
        String[] newcolumn = new String[]{"Bname"};
        String[] newtype = new String[]{"String"};
        ResultSet rsnewtemp ;
        rsnewtemp = qg.selectQuery(sqltop);
        int countnew = 0;
        try {
            while(rsnewtemp.next())
            {
                countnew++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
        String[][] sqlnewData = new String[count][newcolumn.length];
        
        objDisplay.setLzistCol(newcolumn);
        sqlnewData = objDisplay.getData(rsnew, newtype, countnew, newcolumn.length);
        
        //add to jtable
        dtmnew = (DefaultTableModel)tbnew.getModel();
        for(int i = 0; i<count; i++)
        {
            dtmnew.addRow(sqlnewData[i]);
        }
        
        //category
        String sqlCate = "SELECT * FROM Category ";
        ResultSet rsCate;
        ResultSet rsCatetemp;
        rsCate = qg.selectMultiTable(sqlCate);
        rsCatetemp = qg.selectMultiTable(sqlCate);
        
        int countcate = 0;
        try {
            while(rsCatetemp.next())
            {
                countcate++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
        }
        sqlcateData = new String[count][newcolumn.length];
        
        objDisplay.setLzistCol(newcolumn);
        sqlcateData = objDisplay.getData(rsCate, new String[]{"String","String"}, countcate, 2);
        
        for(int i=0; i < sqlcateData.length; i++)
        {
            cbxCategory.addItem(sqlcateData[i][1]);
        }
        
